package com.pwdgame.bean;



public class ActionBarIcon{
	public ActionBarIcon(Object icon, int width, int height) {
		this.icon = icon;
		this.width = width;
		this.height = height;
	}
	public ActionBarIcon(Object icon, int width, int height,Object reserve) {
		this.icon = icon;
		this.width = width;
		this.height = height;
		this.reserve=reserve;
	}
	public Object icon;
	public int width;
	public int height;
	public Object reserve;
}